package tresEnRaya;

public class Ficha {
	private String ficha;
	
	// Inicializa ficha
	public Ficha(String ficha) {
		this.ficha = ficha;
	}
	
	// Devuelve un String con la ficha
	public String getFicha() {
		return this.ficha;
	}
	
	// Devuelve una cadena conteniendo la ficha
	public String toString() {
		return this.ficha;
	}
}
